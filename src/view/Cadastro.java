    private void btnProdutoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnProdutoExcluirActionPerformed
        try{
            int response = JOptionPane.showConfirmDialog(null, "Deseja deletar o usuário? Ação não reversível!");
            if(response==0){
                int code = Integer.parseInt(txtExcluirProduto.getText()), aux = 0;
                if(code <= 0){
                    throw new IntInvalido();
                }
                else{
                    ArrayList<Produto>produtos = this.plControl.getProducts();
                    for(Produto p: produtos){
                        if(p.getCodigo() == code){
                            aux = 1;
                        }
                    }
                    if(aux == 1){
                        plControl.excluirProduto(code);
                        tbl1.setNumRows(0);
                        tblClients.setModel(tbl1);
                        JOptionPane.showMessageDialog(null, "Produto excluído com sucesso!");
                    }
                    else if(aux != 1){
                        throw new ItemInexistente();
                    }
                }
            }
        }
        catch(IntInvalido e){
            JOptionPane.showMessageDialog(null, e.getMessage() );
            txtExcluirProduto.requestFocus();
        }
        catch(ItemInexistente e){
            JOptionPane.showMessageDialog(null,"O produto " + e.getMessage() );
            txtExcluirProduto.requestFocus();
        }
	catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Digite um código válido, apenas com números!");
        }
    }//GEN-LAST:event_btnProdutoExcluirActionPerformed

