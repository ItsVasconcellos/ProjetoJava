    private void btnCadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastroActionPerformed
        try{
            String nome=txtNome.getText();
            String cpf=txtCpf.getText();
            String email = txtEmail.getText();
            String cell = txtTelefone.getText();
            if("".equals(cell) || "".equals(email) || "".equals(cpf) || "".equals(nome)){
                throw new StringNullException();
            }
            ArrayList<Cliente>clients = this.clControl.getClient();
            for(Cliente c: clients){
                if(c.getCpf().equals(cpf)){
                    throw new CpfCadastrado();
                }
            }
            int idade=Integer.parseInt(txtIdade.getText());
            if(idade <= 0){
                throw new IntInvalido();
            }
            if(jComboBoxGen.getSelectedIndex() > 0 && jComboBoxBanda.getSelectedIndex() > 0 ){
                String genre = (String)jComboBoxGen.getSelectedItem();
                String band = (String)jComboBoxBanda.getSelectedItem();
                clControl.cadastrar(nome, cpf, idade, genre, band, email, cell);
                JOptionPane.showMessageDialog(null,"Cliente cadastrado com sucesso!");
                ClearFields();
            }
            else{
                throw new ComboBoxNull();
            }
            
        }
        catch(ComboBoxNull e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        catch(IntInvalido e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Não insira texto em campos que pedem idade!");
        }
        catch(StringNullException e){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos antes de cadastrar!");
        }
        catch(CpfCadastrado e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
    }//GEN-LAST:event_btnCadastroActionPerformed

